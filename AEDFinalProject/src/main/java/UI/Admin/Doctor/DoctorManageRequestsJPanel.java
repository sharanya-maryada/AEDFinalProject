    private ImageIcon setPicturebyte(byte[] byteArrayImage) {

        ImageIcon imageCar;
        imageCar = new ImageIcon(byteArrayImage);
        Image picCar = imageCar.getImage();
        JLabel pictureLabel1 = new JLabel();
        pictureLabel1.setSize(107, 133);
        Image resizedImage = picCar.getScaledInstance(pictureLabel1.getWidth(), pictureLabel1.getHeight(), Image.SCALE_SMOOTH);
        ImageIcon selectedCarPicture = new ImageIcon(resizedImage);
        return selectedCarPicture;
    }

    public void populateTable() {

        DefaultTableModel model = (DefaultTableModel) DoctorJTable.getModel();

        model.setRowCount(0);

        for (Connection connection : docOrganization.getConnectionDirectory().getConnectionList()) {

            if (connection.getDoctorStatus().equals("Requested to Doctor") || connection.getDoctorAssigned().getUsername() == userAccount.getUsername()) {

                Object[] row = new Object[5];
                row[0] = connection.getDonorRequest().getUser().getName();
                row[1] = connection;
                row[2] = connection.getReceiverRequest().getUser().getName();
                if (connection.getDoctorStatus().equals("Requested to Doctor")) {
                    row[3] = "unassinged";
                } else {
                    row[3] = connection.getDoctorAssigned().getUsername();
                }
                row[4] = connection.getDoctorStatus();

                model.addRow(row);

            }
        }

    }

