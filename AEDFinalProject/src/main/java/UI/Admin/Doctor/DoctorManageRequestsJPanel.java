    private void assignJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_assignJButtonActionPerformed

        int selectedRow = DoctorJTable.getSelectedRow();

        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select a row");
            return;
        }

        if (DoctorJTable.getValueAt(selectedRow, 3) == "unassinged") {

            Connection connection = (Connection) DoctorJTable.getValueAt(selectedRow, 1);

            connection.getDonorRequest().setOverallStatus("Assigned to Doctor " + userAccount.getUsername());
            connection.getReceiverRequest().setOverallStatus("Assigned to Doctor " + userAccount.getUsername());

            connection.setDoctorAssigned(userAccount);
            connection.setDoctorStatus("In progress");
            populateTable();
        } else {
            JOptionPane.showMessageDialog(null, "The request is already assigned");

        }


    }//GEN-LAST:event_assignJButtonActionPerformed

    private void approveJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_approveJButtonActionPerformed
        int selectedRow = DoctorJTable.getSelectedRow();

        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select a row");
            return;
        }

        if (DoctorJTable.getValueAt(selectedRow, 4).equals("Completed") || DoctorJTable.getValueAt(selectedRow, 4).equals("Rejected")) {
            JOptionPane.showMessageDialog(null, "Request is either Completed or Rejected ", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (DoctorJTable.getValueAt(selectedRow, 3) == "unassinged") {
            JOptionPane.showMessageDialog(null, "Please assign the request to yourself and then proceed", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }

        Connection connection = (Connection) DoctorJTable.getValueAt(selectedRow, 1);

        connection.getDonorRequest().setOverallStatus("Donation Completed");
        connection.getReceiverRequest().setOverallStatus("Received Organ");
        connection.setDoctorStatus("Completed");
        connection.setInventoryStatus("Approved By Doctor, Donation Complete");
        celebrateButton.setEnabled(true);
        JOptionPane.showConfirmDialog(null, "Donation is complete", "Info", JOptionPane.INFORMATION_MESSAGE);
        populateTable();


    }//GEN-LAST:event_approveJButtonActionPerformed

