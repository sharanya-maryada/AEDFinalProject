    private void createOrgButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createOrgButtonActionPerformed
        // TODO add your handling code here:
        //directory.createDonorBankOrganization((Type)organizationComboBox.getSelectedItem());
        
        
        if (orgName.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Organization name cannot be empty");
            return;
        }

        for (Organization organization : directory.getOrganizationList()) {

            if (organization.getOrgName().equals(orgName.getText())) {

                JOptionPane.showMessageDialog(null, "Organization name already Exists");
                return;
            }
            
          

        }

        if(enterprise.getEnterpriseType().equals(LegalDepartment)){
            Organization o = directory.createLegalOrganization((LegalType)organizationComboBox.getSelectedItem());
            o.setOrgName(orgName.getText());
            populateTable(); 
            JOptionPane.showMessageDialog(null, "Organization created ");
        }
        else{
            Organization o = directory.createDonorBankOrganization((Type)organizationComboBox.getSelectedItem());
            o.setOrgName(orgName.getText());
            JOptionPane.showMessageDialog(null, "Organization created ");
            organizationComboBox.removeItem(organizationComboBox.getSelectedItem());
            populateTable();
        }
        
        orgName.setText("");
    }//GEN-LAST:event_createOrgButtonActionPerformed

