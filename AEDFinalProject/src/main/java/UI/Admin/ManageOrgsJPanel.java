    private void populateCombo() {
        
        organizationComboBox.removeAllItems();
        
        if(enterprise.getEnterpriseType().equals(LegalDepartment)){
        for(LegalType legalType: LegalType.values()){
            if (legalType.getValue().equals(LegalType.Advocate.getValue())){
                organizationComboBox.addItem(legalType);
            }
        }
        }else{
        for (Type type : Type.values()){
            if (type.getValue().equals(Type.Coordinator.getValue())
                    ||type.getValue().equals(Type.Doctor.getValue())
                    ||type.getValue().equals(Type.InventoryManager.getValue())
                    ||type.getValue().equals(Type.Lab.getValue()))
                organizationComboBox.addItem(type);
        }
    }
    }
private void populateTable() {
        DefaultTableModel model = (DefaultTableModel) organizationJTable.getModel();
        
        model.setRowCount(0);
        
        for (Organization organization : directory.getOrganizationList()){
            Object[] row = new Object[3];
            row[0] = organization.getOrganizationID();
            row[1] = organization.getName();
            row[2] = organization.getOrgName();
            
            model.addRow(row);
        }
    }
