    private void createEnterpriseButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createEnterpriseButtonActionPerformed
        // TODO add your handling code here:
        
        
      if (nameTextField.getText().isEmpty() || cityTextField.getText().isEmpty() || zipTextField.getText().isEmpty() ){
            JOptionPane.showMessageDialog(null, "All fields mandatory" , "Error", JOptionPane.ERROR_MESSAGE);
            return;
      }
     
      for ( Network network :ecosystem.getNetworkList()){
      
      if( network.getName().equals(nameTextField.getText())) {
       JOptionPane.showMessageDialog(null, "Network name cannot be same" , "Error", JOptionPane.ERROR_MESSAGE);
       return;       
      
      }
       
      }
               
       
      String networkName      = nameTextField.getText();  
      String networkCity      = cityTextField.getText();  
      String networkZip      = zipTextField.getText();  
      
      
      if (networkName.length() > 0) {
            
      Network network = ecosystem.createAndAddNetwork();
      
      network.setName(networkName);
      network.setCity(networkCity);
      network.setZipcode(networkZip);
      
      JOptionPane.showMessageDialog(null, "Network Created Successfully");
      
      nameTextField.setText("");
      cityTextField.setText("");
      zipTextField.setText("");
      populateTable();
      }
      else {
      
           JOptionPane.showMessageDialog(null, "Please mention the network name");
      
      }
    }//GEN-LAST:event_createEnterpriseButtonActionPerformed

